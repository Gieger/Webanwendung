function backcolorBlack() {
	document.getElementsByTagName("article")[0].style.backgroundColor="000000";
	document.getElementsByTagName("article")[0].style.color="FFFFFF";
	document.getElementsByTagName("table")[0].style.color="FFFFFF";
}
function backcolorNight() {
	document.getElementsByTagName("article")[0].style.backgroundColor="CCFF66";
	document.getElementsByTagName("article")[0].style.color="000000";
	document.getElementsByTagName("table")[0].style.color="000000";
	
}