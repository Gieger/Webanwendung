package com.mvc.bean;
public class EventBean
 {
	//Variabel deklaration
	private String evis;
	private String evbe;
	private String ausgabe;
	//get und set für evis
	public String getEvis()
	{
		return evis;
	}
	public void setEvis(String evis)
	{
		this.evis = evis;
	}
	// get und set für evbe
	public String getEvbe()
	{
		return evbe;
	}
	public void setEvbe(String evbe)
	{
		this.evbe = evbe;
	}
	//get und set für ausgabe
	public String getAusgabe()
	{
		return ausgabe;
	}
	public void setAusgabe(String ausgabe)
	{
		this.ausgabe = ausgabe;
	}
 }